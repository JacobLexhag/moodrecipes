// Function to get a random response for a given mood
function getRandomResponse(mood, recipeName) {
  const moodResponses = responses[mood] || responses.error;
  const randomResponse =
    moodResponses[Math.floor(Math.random() * moodResponses.length)];
  return recipeName
    ? randomResponse.replace("{recipe}", recipeName)
    : randomResponse;
}

